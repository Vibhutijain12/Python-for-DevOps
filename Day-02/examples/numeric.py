## Insert the correct syntax to convert x into a floating point number.
x = 5
x = float(x)
print(x)   ## output: 5.0

## Insert the correct syntax to convert x into a integer.

x = 5.55
x = int(x) 
print(x)   ## output: 5

## Insert the correct syntax to convert x into a complex number.

x = 5 
x = complex(x)
print(x)   ## output: (5+0j)
